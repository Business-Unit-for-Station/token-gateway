export function mockUsagePage(page: number, pageSize: number): PaginatedResponse<UsageLog> {
  const start = Math.max(0, (page - 1) * pageSize)
  const items = mockUsageLogs.slice(start, start + pageSize)
  const pages = Math.max(1, Math.ceil(mockUsageLogs.length / pageSize))
  return {
    items,
    total: mockUsageLogs.length,
    page,
    page_size: pageSize,
    pages,
  }
}
